package pojo;

public class LocationPojo {

	
	private double lat;
	private double lng;
	
	//Alt + Shift + S or Click Source->Generate getters and setters 
	
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	
	
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
	
	
	
}
